package com.wplatform.wizardcraft.attribute;

public enum AggregateType {
    /// <summary>
    /// Adds the value to the raw base value.
    /// </summary>
    AddRaw,

    /// <summary>
    /// Multiplicates the value.
    /// </summary>
    Multiplicate,

    /// <summary>
    /// Adds the value to the final value.
    /// </summary>
    AddFinal
}